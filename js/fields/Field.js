/**
 * Field class
 * @param {number} value
 */
class Field {
    add(element1, element2) {
        return element1 + element2;
    }

    sub(element1, element2) {
        return element1 - element2;
    }

    mul(element1, element2) {
        return element1 * element2;
    }

    div(element1, element2) {
        return element1 / element2;
    }

    eq(element1, element2) {
        return element1 === element2;
    }
}